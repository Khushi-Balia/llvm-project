#ifndef PRUINSTPRINTER_H
#define PRUINSTPRINTER_H
#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class TargetMachine;

class PRUInstPrinter : public MCInstPrinter {
public:
  PRUInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                 const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  virtual void printInst(const MCInst *MI, raw_ostream &OS, StringRef Annot,
                         const MCSubtargetInfo &STI) override;

  virtual void printRegName(raw_ostream &OS, unsigned RegNo) const override;
  
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printMemSrcOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printPCRelImmOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printCCOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);

  // Autogenerated by tblgen.
  void printInstruction(const MCInst *MI, raw_ostream &O);
  
  static const char *getRegisterName(unsigned RegNo);

};
} // end namespace llvm

#endif
